    // Получить случайный набор вопросов с фильтрацией по категории
    public static List<(QuestionDto Question, int CorrectIndex, string Explanation)>
        GetRandomSet(int count, QuizCategory category = QuizCategory.All)
    {
        var pool = category == QuizCategory.All
            ? Questions
            : Questions.Where(q => q.Question.CategoryEnum == category).ToList();

        var rng = new Random();
        return pool.OrderBy(_ => rng.Next()).Take(count).ToList();
    }
